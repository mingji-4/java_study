package javaStudy;

// 기본형 타입 
public class PrimotiveDataTypeExam {

	public static void main(String[] args) {
		// 논리형 데이터 타입 
		boolean isFun = true;
		// boolean isFun = false;
		System.out.println(isFun);
		
		// 문자 데이터 타입 : 작은 따옴표 + 한글자만 사용 가능 
		char c = 'f';
		
		// 정수 
		int x = 59;
		
		// 긴 정수 : 숫자 뒤에 L(대, 소문자 구분 없음)
		long bing = 3445455433l;
		
		// 실수 : 숫자 뒤에 F(대, 소문자 구분 없음)
		float f = 32.4f;
		
		// 큰 실수 
		double d = 3434343.5;
	}

}
